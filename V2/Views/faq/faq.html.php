            <h3>Comment est calculé le forfait kilométrique d’un véhicule avec chauffeur ?</h3>
            <p class="answer">
                Le forfait kilométrique est calculé en fonction du trajet entre notre garage et le point de prise en charge du véhicule, puis du retour à notre dépôt. Selon le véhicule, le départ peut être effectué depuis des points différents.
            </p>
