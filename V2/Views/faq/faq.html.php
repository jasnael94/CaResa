            <h3>Comment fonctionne la franchise lors de la location ?</h3>
            <p class="answer">
                La franchise est une somme d'argent prélevée sur la carte du conducteur principal, servant à limiter sa responsabilité en cas de sinistre. Un rachat partiel de la franchise est disponible, moyennant un supplément. Le montant de la franchise est utilisé pour couvrir les frais en cas de dommages.
            </p>
        </div>

        <div class="faq-item">
            <h3>Qu'est-ce qu'une caution ?</h3>
            <p class="answer">
                La caution est le montant que nous bloquons temporairement sur la carte du conducteur principal pour garantir la franchise en cas de sinistre. Elle est libérée une fois le véhicule restitué et vérifié conforme.
            </p>
        </div>

