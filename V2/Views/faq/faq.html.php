            <h3>Comment fonctionne la franchise lors de la location ?</h3>
            <p class="answer">
                La franchise est une somme d'argent prélevée sur la carte du conducteur principal, servant à limiter sa responsabilité en cas de sinistre. <br>Un rachat partiel de la franchise est disponible, moyennant un supplément. <br> Le montant de la franchise est utilisé pour couvrir les frais en cas de dommages.
            </p>
        </div>

        <div class="faq-item right">
            <h3>Qu'est-ce qu'une caution ?</h3>
            <p class="answer">
                La caution est le montant que nous bloquons temporairement sur la carte du conducteur principal pour garantir la franchise en cas de sinistre. <br> Elle est libérée une fois le véhicule restitué et vérifié conforme.
            </p>
        </div>

        <div class="faq-item left">
            <h3>Comment est calculé le forfait kilométrique d’un véhicule avec chauffeur ?</h3>
            <p class="answer">
                Le forfait kilométrique est calculé en fonction du trajet entre notre garage et le point de prise en charge du véhicule, puis du retour à notre dépôt. <br> Selon le véhicule, le départ peut être effectué depuis des points différents.
            </p>
